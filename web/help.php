</span></span></b><b><span style='font-family:"Arial",sans-serif;color:black'>Primer
Finder  </span></b></p>

<p class=MsoNormal style='margin-bottom:11.25pt;text-align:justify;background:
white'><span style='font-family:"Arial",sans-serif;color:black'>The Primer
Finder is a web-based tool that enables <i>in silico</i></span><span
style='font-family:"Arial",sans-serif;color:black'> testing of the performance
of PCR and RT-PCR primers used for ARG diagnostics. It will support multiple
use cases (ARG-specific primers and flanking gene-specific primers). Unless the
user specifies an input sequence or GenBank accession to test (see screenshot
below), Primer Finder will first check primer-binding to ARGs internal to the
AMR-DB (default) from a user-provided multi-FASTA sequence of primers or pasted
sequence of forward and reverse primers. Primer Finder will produce an output
report of the predicted binding sites, target(s) hit, amplicon sequence,
length, position, number of degenerate bases and mismatches per primer, strand,
gene annotations overlapping the amplicon and amplicon FASTA file. If the user
specifies an input sequence or GenBank accession to query primer-binding sites,
possibly to check primers external to ARGs (a.k.a., flanking primers), Primer
Finder will first find the ARGs on the input sequence using our newly-developed
AMR-Finder, and annotate ARGs within the input sequence, search for
primer-binding sites and generate a report. The produced report displays
multiple binding sites for primers on the specified genome(s). When using
user-supplied sequences to query, it’s critical to have as much of the target
genome (in the case of single isolate screening by PCR) provided so that
primer-binding specificity and amplicon length can be verified.</span></p>

